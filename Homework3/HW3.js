let day = prompt("Введите день недели").toLowerCase();

if (day === "вторник" || day === "четверг" || day === "суббота") {
  console.log("Это четный день недели");
} else if (
  day === "понедельник" ||
  day === "среда" ||
  day === "пятница" ||
  day === "воскресенье"
) {
  console.log("Это нечетный день недели");
} else {
  console.log("Такого дня недели нет");
}
